#Prog09: Create a program that print all the even numbers starting from 0 to 100. (Use for loop)
 
#makes an empty list where all numbers from 0 to 100 will be placed
one_to_100 = []

#makes a loop that appends(inputs) numbers 0 to 100 to the empty list
for x in range(101):
    one_to_100.append(x)

#makes another empty list for all even numbers this time
even = []

#makes another loop that detects which numbers are even in the 1st list
for i in one_to_100:
    if i %2 == 0:
        even.append(i)

#prints the list with all the even numbers
print(even)
