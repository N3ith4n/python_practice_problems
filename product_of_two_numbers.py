#Prog04: Create a program that ask user to input 2 numbers. Print the product of the two numbers.
 
#Asks the user for the two numbers and directly converts it into an integer
n1 = int(input("Give me some numbers, first one = "))
n2 = int(input("second one = "))

#The program multiplies the two numbers and prints the product
