#Prog05: Create a program that ask user to input 2 numbers. Print the quotient of the two numbers with the decimal point
 
#Asks the user for the two numbers and directly converts it into a float (decimal number)
n1 = float(input("Give me some numbers, first one = "))
n2 = float(input("second one = "))
 
#The program divides the two numbers and prints the quotient with the decimal point
quotient = n1 / n2
print(quotient)
